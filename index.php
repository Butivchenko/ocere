                totGBP = 0;
                totCount = 0;
                $('.form-new input[type="number"]').each(function () {
                    val = $(this).val();
                    rel = $(this).attr('rel');
                    if (val > 0) {
                        price = $(this).data('price');
                        gbp = val * price;

                        totGBP = gbp * 1 + totGBP * 1;
                        $(this).addClass('quantity-selected');
                    } else {
                        $(this).removeClass('quantity-selected');
                    }
                    totCount = totCount + val * 1;

                    if (val > 0)
                        $('.desc-' + rel).show();
                    else
                        $('.desc-' + rel).hide();

                    curCount = $('.r' + rel).length;
                    if (curCount < val) {
                        for (i = 1; i <= val - curCount; i++)
                            $('.desc-' + rel).clone().removeClass('desc-' + rel).addClass('r' + rel + '-' + i).insertBefore($('.desc-' + rel));
                    } else {
                        for (i = curCount; i >= val; i--)
                            $('.r' + rel + '-' + i).remove();
                    }
                    /*} else {
                        $('.desc-'+rel).hide();
                        $('.r'+rel).remove();
                    }*/
                });
                totUSD = totGBP * 1.29;

                $('.totUSD').text(totUSD.toFixed(2));
                $('.totGBP').text(totGBP);
                expectedDate = '';
                if (totCount < 25) {
                    expectedDate = "Expected Turnaround: Approx 14 Days";
                } else if (totCount >= 25) {
                    expectedDate = "Expected Turnaround: Approx 28 Days";
                }

                $('.expectedDate').text(expectedDate);
            })

